package com.expo.coins;

public class CoinPair {

	private Coin targetCoin;
	private Coin baseCoin;

	public Coin getTargetCoin() {
		return targetCoin;
	}

	public void setTargetCoin(Coin targetCoin) {
		this.targetCoin = targetCoin;
	}

	public Coin getBaseCoin() {
		return baseCoin;
	}

	public void setBaseCoin(Coin baseCoin) {
		this.baseCoin = baseCoin;
	}

}
